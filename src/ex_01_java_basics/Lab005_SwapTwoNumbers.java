package ex_01_java_basics;

public class Lab005_SwapTwoNumbers {
    public static void main(String[] args) {

        //By using third variable

        int a = 10;
        int b = 20;

        int temp = a;
        a = b;
        b = temp;

        System.out.println("a = "+a);
        System.out.println("b = "+b);


    }
}
